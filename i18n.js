// ═══ Language system ═══
let currentLang = localStorage.getItem('kanjipro_lang') || 'en';

function setLang(lang) {
  currentLang = lang;
  localStorage.setItem('kanjipro_lang', lang);
  if (typeof onLangChange === 'function') onLangChange();
}

function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || I18N.en[key] || key;
}

// Get kanji meaning in current language
function getM(kanjiObj) {
  if (currentLang === 'pt' && kanjiObj.m_pt) return kanjiObj.m_pt;
  if (currentLang === 'es' && kanjiObj.m_es) return kanjiObj.m_es;
  return kanjiObj.m || '';
}

// Get example meaning in current language
function getExM(exObj) {
  if (currentLang === 'pt' && exObj.m_pt) return exObj.m_pt;
  if (currentLang === 'es' && exObj.m_es) return exObj.m_es;
  return exObj.m || '';
}

